function toRecord([year, title, notionId], index) {
  const dateMatch = title.match(/^(\d{1,2})\.(\d{1,2})/);
  const month = Number(dateMatch[1]);
  const day = Number(dateMatch[2]);
  const tokens = [...title.matchAll(/\$([A-Za-z0-9]+)/g)].map((match) => match[1]);
  const primary = (tokens[0] || "UNKNOWN").toUpperCase();
  const chains = PAGE_CHAIN_OVERRIDES[notionId] || [];
  const featured = FEATURED_NOTES[primary] || {};

  return {
    id: index + 1,
    year,
    month,
    day,
    date: `${year}-${String(month).padStart(2, "0")}-${String(day).padStart(2, "0")}`,
    title,
    tokens,
    primary,
    project: featured.project || tokens.join(" / "),
    chains: chains.length ? chains : ["Other"],
    chainVerified: chains.length > 0,
    summary: featured.summary || "完整原始研究记录已归档于 Notion。链与结构化指标等待后续校对。",
    notionUrl: `https://dune-marten-78b.notion.site/${notionId}`,
  };
}

